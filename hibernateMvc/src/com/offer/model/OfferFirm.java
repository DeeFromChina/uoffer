package com.offer.model;

import java.util.Date;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "offer_firm")
public class OfferFirm {

   @Id
   @GeneratedValue
   @Column(name = "id")
   private Integer id;

   @Column(name = "offer_name")
   private String  offerName;

   @Column(name = "offer_email")
   private String  offerEmail;

   @Column(name = "status")
   private String  status;

   @Column(name = "create_time")
   private Date    createTime;

   @Column(name = "endless")
   private String  endless;

   @Column(name = "admin_id")
   private Integer adminId;

   @Column(name = "offer_adviser_remark")
   private String  offerAdviserRemark;

   public Integer getId() {
      return id;
   }

   public void setId(Integer id) {
      this.id = id;
   }

   public String getOfferName() {
      return offerName;
   }

   public void setOfferName(String offerName) {
      this.offerName = offerName;
   }

   public String getOfferEmail() {
      return offerEmail;
   }

   public void setOfferEmail(String offerEmail) {
      this.offerEmail = offerEmail;
   }

   public String getStatus() {
      return status;
   }

   public void setStatus(String status) {
      this.status = status;
   }

   public Date getCreateTime() {
      return createTime;
   }

   public void setCreateTime(Date createTime) {
      this.createTime = createTime;
   }

   public String getEndless() {
      return endless;
   }

   public void setEndless(String endless) {
      this.endless = endless;
   }

   public Integer getAdminId() {
      return adminId;
   }

   public void setAdminId(Integer adminId) {
      this.adminId = adminId;
   }

   public String getOfferAdviserRemark() {
      return offerAdviserRemark;
   }

   public void setOfferAdviserRemark(String offerAdviserRemark) {
      this.offerAdviserRemark = offerAdviserRemark;
   }
}
