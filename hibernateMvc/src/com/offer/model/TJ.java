package com.offer.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "tj")
public class TJ {

	@Id
	@GeneratedValue
	@Column(name = "id")
	private String id;

	@Column(name = "TJ_T")
	private String TJT;

	@Column(name = "TJ_S")
	private String TJS;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTJT() {
		return TJT;
	}

	public void setTJT(String tJT) {
		TJT = tJT;
	}

	public String getTJS() {
		return TJS;
	}

	public void setTJS(String tJS) {
		TJS = tJS;
	}

}
